    fi

    pkg_install php php-cli php-fpm php-mysqlnd php-curl php-xml php-mbstring php-zip php-gd php-intl php-soap php-opcache php-bcmath php-ldap
    PHP_BIN="/usr/bin/php"
    PHP_FPM_SERVICE="php-fpm"
