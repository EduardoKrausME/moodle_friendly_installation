load_progress() {
    if [[ "${RESET_INSTALLER_PROGRESS:-0}" == "1" ]]; then
        rm -f "${PROGRESS_FILE}" 2>/dev/null || true
        return 0
    fi

    [[ -f "${PROGRESS_FILE}" ]] || return 0

    # shellcheck source=/dev/null
    source "${PROGRESS_FILE}"

    # Explicit environment variables win over saved progress.
    [[ -n "${REQUESTED_DB_ENGINE:-}" ]] && DB_ENGINE="${REQUESTED_DB_ENGINE}"
    [[ -n "${REQUESTED_PANEL_DOMAIN:-}" ]] && PANEL_DOMAIN="${REQUESTED_PANEL_DOMAIN}"
    [[ -n "${REQUESTED_DB_ROOT_PASS:-}" ]] && DB_ROOT_PASS="${REQUESTED_DB_ROOT_PASS}"
    [[ -n "${REQUESTED_LE_EMAIL:-}" ]] && LE_EMAIL="${REQUESTED_LE_EMAIL}"

    log "Loaded installer progress from ${PROGRESS_FILE}"
}

mark_progress_done() {
    local var_name="$1"
    printf -v "${var_name}" '%s' "1"
    save_progress
}

open_interactive_terminal() {
    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        return 0
    fi

    if [[ ! -e /dev/tty ]]; then
        die "Interactive installation requires a TTY. Run from SSH/terminal, or use NONINTERACTIVE=1 with DB_ENGINE and PANEL_DOMAIN."
    fi

    # Keep a stable terminal handle. This avoids prompts disappearing or read blocking
    # when the installer itself was started through curl | sudo bash.
    exec 3</dev/tty || die "Could not open /dev/tty for reading."
    exec 4>/dev/tty || die "Could not open /dev/tty for writing."
    TTY_IN_FD_OPENED=1
}

configure_selinux_permissive() {
    local state=""
    local selinux_active="0"

    if command_exists getenforce; then
        state="$(getenforce 2>/dev/null || true)"
        if [[ "${state}" == "Enforcing" || "${state}" == "Permissive" ]]; then
            selinux_active="1"
        fi
    elif [[ -f /etc/selinux/config ]] && grep -qiE '^[[:space:]]*SELINUX=(enforcing|permissive)[[:space:]]*$' /etc/selinux/config; then
        selinux_active="1"
    fi

    [[ "${selinux_active}" == "1" ]] || return 0

    if [[ "${state}" == "Enforcing" ]]; then
        log "SELinux is active in enforcing mode. Switching the current session to permissive."
        setenforce 0 || warn "Could not execute setenforce 0. Continuing, but SELinux may still block services."
    else
        log "SELinux is active. Ensuring permanent mode is permissive."
    fi

    if [[ -f /etc/selinux/config ]]; then
        if grep -qE '^[[:space:]]*SELINUX=' /etc/selinux/config; then
            sed -i -E 's/^[[:space:]]*SELINUX=.*/SELINUX=permissive/' /etc/selinux/config || warn "Could not update /etc/selinux/config."
        else
            printf '\nSELINUX=permissive\n' >> /etc/selinux/config || warn "Could not append SELINUX=permissive to /etc/selinux/config."
        fi
    else
        warn "/etc/selinux/config was not found. Runtime SELinux mode was handled only for the current boot."
    fi
}

prompt_text() {
    local var_name="$1"
    local label="$2"
    local default_value="${3:-}"
    local value=""

    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        value="${!var_name:-${default_value}}"
    else
        [[ "${TTY_IN_FD_OPENED}" == "1" ]] || open_interactive_terminal

        if [[ -n "${default_value}" ]]; then
            printf '%s [%s]: ' "${label}" "${default_value}" >&4
            if ! IFS= read -r value <&3; then
                die "Could not read input from the terminal."
            fi
            value="${value:-${default_value}}"
        else
            printf '%s: ' "${label}" >&4
            if ! IFS= read -r value <&3; then
                die "Could not read input from the terminal."
            fi
        fi
    fi

    printf -v "${var_name}" '%s' "${value}"
}

prompt_secret() {
    local var_name="$1"
    local label="$2"
    local value=""

    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        value="${!var_name:-}"
        [[ -n "${value}" ]] || die "Variable ${var_name} must be defined when NONINTERACTIVE=1."
    else
        [[ "${TTY_IN_FD_OPENED}" == "1" ]] || open_interactive_terminal
        printf '%s: ' "${label}" >&4
        if ! IFS= read -r -s value <&3; then
            printf '
' >&4
            die "Could not read password from the terminal."
        fi
        printf '
' >&4
    fi

    printf -v "${var_name}" '%s' "${value}"
}

ui_available() {
    [[ "${NONINTERACTIVE}" != "1" ]] && [[ -e /dev/tty ]] && command_exists whiptail
}

prompt_database_select() {
    if [[ -n "${DB_ENGINE:-}" ]]; then
        DB_ENGINE="$(printf '%s' "${DB_ENGINE}" | tr '[:upper:]' '[:lower:]')"
        case "${DB_ENGINE}" in
            mariadb|mysql)
                log "Using saved database option: ${DB_ENGINE}"
                save_progress
                return 0
                ;;
            *)
                die "Invalid saved database option: ${DB_ENGINE}. Use mariadb or mysql."
                ;;
        esac
    fi

    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        DB_ENGINE="${DB_ENGINE:-mariadb}"
        DB_ENGINE="$(printf '%s' "${DB_ENGINE}" | tr '[:upper:]' '[:lower:]')"
    elif ui_available; then
        local choice=""
        choice="$(whiptail             --title "Moodle Friendly Installation"             --menu "Database to install"             14 78 2             "mysql" "MySQL ${MYSQL_REQUIRED:+>= ${MYSQL_REQUIRED}}"             "mariadb" "MariaDB ${MARIADB_REQUIRED:+>= ${MARIADB_REQUIRED}}"             3>&1 1>&2 2>&3 < /dev/tty)" || die "Database selection cancelled."
        DB_ENGINE="${choice}"
    else
        [[ "${TTY_IN_FD_OPENED}" == "1" ]] || open_interactive_terminal
        local options=("mariadb" "mysql")
        local choice=""
        printf '
Database to install:
' >&4
        PS3="Select an option [1-2]: "
        select choice in "${options[@]}"; do
            case "${choice}" in
                mariadb|mysql)
                    DB_ENGINE="${choice}"
                    break
                    ;;
                *)
                    printf 'Invalid option. Select 1 or 2.
' >&4
                    ;;
            esac
        done <&3 >&4 2>&4
    fi

    case "${DB_ENGINE}" in
        mariadb|mysql) ;;
        *) die "Invalid database option: ${DB_ENGINE}. Use mariadb or mysql." ;;
    esac

    save_progress
}

prompt_domain_box() {
    if [[ "${PANEL_DOMAIN_SELECTED:-0}" == "1" && "${NONINTERACTIVE}" != "1" ]]; then
        if [[ -n "${PANEL_DOMAIN:-}" ]]; then
            log "Using saved panel access option: ${PANEL_DOMAIN}"
        else
            log "Using saved panel access option: public IP"
        fi
        return 0
    fi

    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        PANEL_DOMAIN="${PANEL_DOMAIN:-}"
    elif ui_available; then
        local value=""
        value="$(whiptail             --title "Moodle Friendly Installation"             --inputbox "Panel domain, or leave blank to use the public IP."             10 78 "${PANEL_DOMAIN:-}"             3>&1 1>&2 2>&3 < /dev/tty)" || die "Domain input cancelled."
        PANEL_DOMAIN="${value}"
    else
        prompt_text PANEL_DOMAIN "Panel domain, or press ENTER to use the public IP" ""
    fi

    PANEL_DOMAIN="$(printf '%s' "${PANEL_DOMAIN}" | tr '[:upper:]' '[:lower:]' | sed -E 's#^https?://##; s#/.*$##; s/:.*$//')"
    PANEL_DOMAIN_SELECTED=1
    save_progress
}

generate_strong_password() {
    python3 - <<'GENPASS'
import random
import secrets

lower = "abcdefghijkmnopqrstuvwxyz"
upper = "ABCDEFGHJKLMNPQRSTUVWXYZ"
digits = "23456789"
symbols = "@#%_-+="
alphabet = lower + upper + digits + symbols
password = [
    secrets.choice(lower),
    secrets.choice(upper),
    secrets.choice(digits),
    secrets.choice(symbols),
]
password.extend(secrets.choice(alphabet) for _ in range(28))
random.SystemRandom().shuffle(password)
print("".join(password))
GENPASS
}

detect_os() {
    [[ -r /etc/os-release ]] || die "Could not detect the Linux distribution."
    # shellcheck source=/dev/null
    source /etc/os-release
    OS_ID="${ID:-}"
    OS_VERSION_ID="${VERSION_ID:-}"
    OS_LIKE="${ID_LIKE:-}"

    case "${OS_ID} ${OS_LIKE}" in
        *fedora*)
            OS_FAMILY="fedora"
            PKG_MANAGER="dnf"
            ;;
        *centos*|*rhel*|*rocky*|*almalinux*)
            OS_FAMILY="rhel"
            if command_exists dnf; then
                PKG_MANAGER="dnf"
            elif command_exists yum; then
                PKG_MANAGER="yum"
            else
                die "RHEL-family system detected, but neither dnf nor yum is available."
            fi
            ;;
        *)
            die "This installer supports Fedora/CentOS/RHEL/AlmaLinux/Rocky only. Detected: ID=${OS_ID}, ID_LIKE=${OS_LIKE}. Use install/installation.sh to auto-select the correct installer."
            ;;
    esac
    log "Detected system: ${OS_ID} ${OS_VERSION_ID} (${OS_FAMILY})"
}

pkg_retry() {
    local output
    local code
    local tries="${PKG_LOCK_RETRIES:-60}"
    local delay="${PKG_LOCK_SLEEP:-5}"
    local i

    for ((i = 1; i <= tries; i++)); do
        output="$("$@" 2>&1)"
        code=$?

        if [[ -n "${output}" ]]; then
            printf '%s\n' "${output}"
        fi

        if [[ "${code}" -eq 0 ]]; then
            return 0
        fi

        if grep -qiE 'Failed to obtain rpm transaction lock|Another transaction is in progress|transaction lock|rpm.*lock|dnf.*lock|yum.*lock' <<< "${output}"; then
            warn "Package manager lock is busy. Retry ${i}/${tries} in ${delay}s..."
            sleep "${delay}"
            continue
        fi

        return "${code}"
    done

    warn "Package manager lock did not become available after ${tries} attempts."
    return 1
}

pkg_update() {
    pkg_retry "${PKG_MANAGER}" makecache -y || true
}

pkg_install() {
    pkg_retry "${PKG_MANAGER}" install -y "$@"
}


install_nodejs_22() {
    log "Installing Node.js 22"

    if command_exists node && node -v 2>/dev/null | grep -qE '^v22\.'; then
        command_exists npm || die "npm was not found with the current Node.js 22 installation."
        command_exists npx || die "npx was not found with the current Node.js 22 installation."
        return 0
    fi

    if [[ "${PKG_MANAGER}" == "dnf" ]]; then
        ${PKG_MANAGER} -y module reset nodejs >/dev/null 2>&1 || true
    fi

    curl -fsSL https://rpm.nodesource.com/setup_22.x -o /tmp/nodesource_setup_22.sh || die "Failed to download NodeSource setup for Node.js 22."
    bash /tmp/nodesource_setup_22.sh || die "Failed to configure NodeSource repository for Node.js 22."
    rm -f /tmp/nodesource_setup_22.sh

    pkg_install nodejs

    command_exists node || die "Node.js was not found after installation."
    command_exists npm || die "npm was not found after Node.js installation."
    command_exists npx || die "npx was not found after Node.js installation."

    if ! node -v 2>/dev/null | grep -qE '^v22\.'; then
        die "Node.js 22 was expected, but detected $(node -v 2>/dev/null || printf 'unknown')."
    fi
}

install_gradle_8_14_2() {
    local gradle_version="8.14.2"
    local gradle_home="/opt/gradle/gradle-${gradle_version}"
    local gradle_zip="/tmp/gradle-${gradle_version}-bin.zip"
    local gradle_url="${GRADLE_URL:-https://services.gradle.org/distributions/gradle-${gradle_version}-bin.zip}"

    log "Installing Gradle ${gradle_version}"

    if command_exists gradle && gradle --version 2>/dev/null | grep -q "Gradle ${gradle_version}"; then
        return 0
    fi

    mkdir -p /opt/gradle
    if [[ ! -x "${gradle_home}/bin/gradle" ]]; then
        curl -fsSL "${gradle_url}" -o "${gradle_zip}" || die "Failed to download Gradle ${gradle_version}."
        rm -rf "${gradle_home}"
        unzip -q "${gradle_zip}" -d /opt/gradle || die "Failed to extract Gradle ${gradle_version}."
        rm -f "${gradle_zip}"
    fi

    ln -sfn "${gradle_home}/bin/gradle" /usr/local/bin/gradle

    cat > /etc/profile.d/gradle.sh <<EOF
export GRADLE_HOME="${gradle_home}"
export PATH="\${GRADLE_HOME}/bin:\${PATH}"
EOF
    chmod 0644 /etc/profile.d/gradle.sh

    export GRADLE_HOME="${gradle_home}"
    export PATH="${GRADLE_HOME}/bin:${PATH}"

    command_exists gradle || die "Gradle command was not found after installation."
}

configure_java_home() {
    local javac_path=""
    local java_home=""

    command_exists javac || die "OpenJDK 17 was not installed correctly because javac was not found."

    javac_path="$(readlink -f "$(command -v javac)")"
    java_home="$(dirname "$(dirname "${javac_path}")")"

    cat > /etc/profile.d/java-17.sh <<EOF
export JAVA_HOME="${java_home}"
export PATH="\${JAVA_HOME}/bin:\${PATH}"
EOF
    chmod 0644 /etc/profile.d/java-17.sh

    export JAVA_HOME="${java_home}"
    export PATH="${JAVA_HOME}/bin:${PATH}"

    java -version >/dev/null 2>&1 || die "Java was not found after setting JAVA_HOME."
}

ensure_imagemagick_magick_command() {
    log "Checking ImageMagick magick command"

    if command_exists magick; then
        return 0
    fi

    if ! command_exists convert; then
        die "ImageMagick was installed, but neither magick nor convert was found."
    fi

    # ImageMagick 6 exposes convert/identify/mogrify instead of the ImageMagick 7 magick entrypoint.
    # This wrapper keeps Cordova/icon scripts compatible on distributions that still package ImageMagick 6.
    cat > /usr/local/bin/magick <<'EOF'
#!/usr/bin/env bash
set -Eeuo pipefail

subcommand="${1:-}"
case "${subcommand}" in
    identify|convert|mogrify|montage|composite|compare|conjure|display|animate|stream|import)
        if command -v "${subcommand}" >/dev/null 2>&1; then
            exec "$@"
        fi
        ;;
esac

exec convert "$@"
EOF
    chmod 0755 /usr/local/bin/magick
}

accept_android_sdk_licenses() {
    local sdkmanager="$1"
    local code=0

    set +o pipefail
    yes | "${sdkmanager}" --licenses >/dev/null
    code=$?
    set -o pipefail

    return "${code}"
}

install_android_sdk_packages() {
    local sdkmanager="$1"
    local sdk_root="$2"
    shift 2
    local code=0

    set +o pipefail
    yes | "${sdkmanager}" --sdk_root="${sdk_root}" --install "$@"
    code=$?
    set -o pipefail

    return "${code}"
}

install_android_sdk() {
    local android_sdk_root="${ANDROID_SDK_ROOT:-/opt/android-sdk}"
    local cmdline_tools_url="${ANDROID_CMDLINE_TOOLS_URL:-https://dl.google.com/android/repository/commandlinetools-linux-14742923_latest.zip}"
    local cmdline_tools_sha256="${ANDROID_CMDLINE_TOOLS_SHA256:-48833c34b761c10cb20bcd16582129395d121b27a99568fbffa64d887684f38f833}"
    local cmdline_tools_zip="/tmp/android-commandlinetools.zip"
    local sdkmanager="${android_sdk_root}/cmdline-tools/latest/bin/sdkmanager"

    log "Installing Android SDK command-line tools and build packages"

    mkdir -p "${android_sdk_root}/cmdline-tools"

    if [[ ! -x "${sdkmanager}" ]]; then
        curl -fsSL "${cmdline_tools_url}" -o "${cmdline_tools_zip}" || die "Failed to download Android SDK command-line tools."

        rm -rf "${android_sdk_root}/cmdline-tools/latest" "${android_sdk_root}/cmdline-tools/cmdline-tools"
        unzip -q "${cmdline_tools_zip}" -d "${android_sdk_root}/cmdline-tools" || die "Failed to extract Android SDK command-line tools."
        mv "${android_sdk_root}/cmdline-tools/cmdline-tools" "${android_sdk_root}/cmdline-tools/latest"
        rm -f "${cmdline_tools_zip}"
    fi

    chown -R root:root "${android_sdk_root}"

    cat > /etc/profile.d/android-sdk.sh <<EOF
export ANDROID_HOME="${android_sdk_root}"
export ANDROID_SDK_ROOT="${android_sdk_root}"
export PATH="\${ANDROID_SDK_ROOT}/cmdline-tools/latest/bin:\${ANDROID_SDK_ROOT}/platform-tools:\${ANDROID_SDK_ROOT}/build-tools/36.0.0:\${PATH}"
EOF
    chmod 0644 /etc/profile.d/android-sdk.sh

    export ANDROID_HOME="${android_sdk_root}"
    export ANDROID_SDK_ROOT="${android_sdk_root}"
    export PATH="${ANDROID_SDK_ROOT}/cmdline-tools/latest/bin:${ANDROID_SDK_ROOT}/platform-tools:${ANDROID_SDK_ROOT}/build-tools/36.0.0:${PATH}"

    [[ -x "${sdkmanager}" ]] || die "sdkmanager was not found after Android SDK command-line tools installation."

    accept_android_sdk_licenses "${sdkmanager}" || warn "Could not pre-accept all Android SDK licenses before package installation. Continuing with package installation."
    install_android_sdk_packages "${sdkmanager}" "${ANDROID_SDK_ROOT}" \
        "platform-tools" \
        "platforms;android-36" \
        "build-tools;36.0.0" || die "Failed to install Android SDK platform 36 and build-tools 36.0.0."
    accept_android_sdk_licenses "${sdkmanager}" || warn "Could not accept all Android SDK licenses after package installation."

    [[ -d "${ANDROID_SDK_ROOT}/platforms/android-36" ]] || die "Android platform android-36 was not installed."
    [[ -d "${ANDROID_SDK_ROOT}/build-tools/36.0.0" ]] || die "Android build-tools 36.0.0 was not installed."
}

install_base_packages() {
    dnf install -y adoptium-temurin-java-repository
    dnf config-manager setopt adoptium-temurin-java-repository.enabled=1 -y
    dnf clean all -y
    dnf makecache --refresh -y
    local base_packages=(
        ca-certificates curl wget gnupg2 unzip zip tar git bind-utils cronie openssl python3 sed grep gawk coreutils newt
        make gcc gcc-c++ temurin-17-jdk ImageMagick
    )

    log "Installing base packages"
    pkg_update
    pkg_install "${base_packages[@]}" policycoreutils-python-utils || pkg_install "${base_packages[@]}"
    systemctl enable --now crond >/dev/null 2>&1 || true

    configure_java_home
    install_nodejs_22
    install_gradle_8_14_2
    ensure_imagemagick_magick_command
    install_android_sdk
}

fetch_moodle_environment() {
    local tmpxml="/tmp/moodle-environment.xml"
    local outenv="/tmp/moodle-env-requirements.env"

    log "Reading current Moodle requirements from environment.xml"
    curl -fsSL "${ENV_URL}" -o "${tmpxml}" || die "Failed to download ${ENV_URL}"

    python3 - "${tmpxml}" > "${outenv}" <<'PY'
import shlex
import sys
import xml.etree.ElementTree as ET

xml_path = sys.argv[1]
root = ET.parse(xml_path).getroot()
moodles = list(root.iter('MOODLE'))
if not moodles:
    raise SystemExit('Nenhum bloco MOODLE encontrado no environment.xml')

moodle = moodles[-1]

def emit(key, value):
    print(f'{key}={shlex.quote(value or "")}')

php_required = ''
for item in moodle.iter('PHP'):
    if item.attrib.get('level') == 'required':
        php_required = item.attrib.get('version', '').strip()

vendors = {}
for vendor in moodle.iter('VENDOR'):
    name = vendor.attrib.get('name', '').strip().lower()
    if name in {'mariadb', 'mysql'}:
        vendors[name] = vendor.attrib.get('version', '').strip()

emit('MOODLE_VERSION', moodle.attrib.get('version', '').strip())
emit('MOODLE_REQUIRES', moodle.attrib.get('requires', '').strip())
emit('PHP_REQUIRED', php_required)
emit('MARIADB_REQUIRED', vendors.get('mariadb', ''))
