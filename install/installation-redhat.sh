}

restart_unit_with_retry() {
    local unit="$1"
    local attempt=1
    local max_attempts=3

    while (( attempt <= max_attempts )); do
        if systemctl restart "${unit}"; then
            return 0
        fi

        warn "Could not restart ${unit} on attempt ${attempt}/${max_attempts}. Retrying..."
        systemctl status "${unit}" --no-pager -l || true
        sleep 2
        attempt=$((attempt + 1))
    done

    journalctl -u "${unit}" -n 80 --no-pager || true
    die "Failed to restart ${unit}."
}

restart_services() {
    log "Validating and restarting services"

    systemctl enable "${PHP_FPM_SERVICE}"
    restart_unit_with_retry "${PHP_FPM_SERVICE}"

    httpd -t

    systemctl enable httpd
    restart_unit_with_retry httpd

    nginx -t

    # Do not use `systemctl enable --now nginx` here. On Fedora/RHEL,
    # `--now` can try to start NGINX before Apache has fully released port 80
    # after being moved to 127.0.0.1:8080, causing a false installer failure.
    systemctl enable nginx
    restart_unit_with_retry nginx
}

prompt_lets_encrypt_email() {
    local default_email="admin@${PANEL_DOMAIN}"
    local value=""

    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        LE_EMAIL="${LE_EMAIL:-${default_email}}"
    elif ui_available; then
        value="$(whiptail \
            --title "Moodle Friendly Installation" \
            --inputbox "Digite o e-mail para registrar o certificado Let's Encrypt." \
            10 78 "${LE_EMAIL:-${default_email}}" \
            3>&1 1>&2 2>&3 < /dev/tty)" || die "Let's Encrypt email input cancelled."

        LE_EMAIL="${value:-${default_email}}"
    else
        prompt_text LE_EMAIL "E-mail para Let's Encrypt" "${LE_EMAIL:-${default_email}}"
    fi

    LE_EMAIL="$(printf '%s' "${LE_EMAIL}" | tr -d '[:space:]')"
}

issue_lets_encrypt() {
    [[ -n "${PANEL_DOMAIN}" ]] || return 0

    prompt_lets_encrypt_email
    [[ "${LE_EMAIL}" =~ ^[^@[:space:]]+@[^@[:space:]]+\.[^@[:space:]]+$ ]] || die "Invalid Let's Encrypt email."

    log "Issuing Let's Encrypt certificate for ${PANEL_DOMAIN}"
    certbot --nginx -d "${PANEL_DOMAIN}" --non-interactive --agree-tos -m "${LE_EMAIL}" --redirect || die "Failed to issue the Let's Encrypt certificate. Check DNS, ports 80/443 and firewall."
    systemctl reload nginx
}

terminal_link() {
    local url="$1"
    local label="${2:-$1}"

    # OSC 8 hyperlink. Supported by many modern terminals.
    printf '\033]8;;%s\033\\%s\033]8;;\033\\' "${url}" "${label}"
}

final_check() {
    local url="${BASE_URL}"
    local apache_service="apache2"
    local curl_ok="0"

    if systemctl list-unit-files httpd.service >/dev/null 2>&1; then
        apache_service="httpd"
    fi

    local reset="\033[0m"
    local bold="\033[1m"
    local green="\033[1;32m"
    local yellow="\033[1;33m"
    local blue="\033[1;34m"
    local cyan="\033[1;36m"
    local white="\033[1;37m"
    local navy="\033[1;34m"
    local blue="\033[0;34m"
    local cyan="\033[0;36m"
    local white="\033[1;37m"
    local green="\033[0;32m"

    local sep="------------------------------------------------------------"

    log "Final test: ${url}"

    if curl -k -fsSI --max-time 15 "${url}" >/dev/null 2>&1; then
        curl_ok="1"
        printf '\n%b==>%b Panel is responding at ' "${green}" "${reset}"
        terminal_link "${url}" "${url}"
        printf '\n'
    else
        warn "Could not validate through curl. Check: systemctl status nginx ${apache_service} ${PHP_FPM_SERVICE}"
    fi

    printf '\n'

    final_banner_line() {
        printf '%b------------------------------------------------------------%b\n' "${cyan}" "${reset}"
    }

    final_banner_points() {
        printf '%b. . . . . . . . . . . . . . . . . . . . . . . . . . . .%b\n' "${yellow}" "${reset}"
    }

    final_banner_animate() {
        local text="$1"
        local color="$2"
        local i

        for ((i = 0; i < ${#text}; i++)); do
            printf '%b%s%b' "${color}" "${text:${i}:1}" "${reset}"
            sleep 0.01
        done
        printf '\n'
    }

    final_banner_line
    final_banner_points
    printf '%b' "${bold}${navy}"
    final_banner_animate "███╗   ███╗ ██████╗  ██████╗ ██████╗ ██╗     ███████╗" "${navy}"
    final_banner_animate "████╗ ████║██╔═══██╗██╔═══██╗██╔══██╗██║     ██╔════╝" "${blue}"
    final_banner_animate "██╔████╔██║██║   ██║██║   ██║██║  ██║██║     █████╗  " "${cyan}"
    final_banner_animate "██║╚██╔╝██║██║   ██║██║   ██║██║  ██║██║     ██╔══╝  " "${green}"
    final_banner_animate "██║ ╚═╝ ██║╚██████╔╝╚██████╔╝██████╔╝███████╗███████╗" "${white}"
    final_banner_animate "╚═╝     ╚═╝ ╚═════╝  ╚═════╝ ╚═════╝ ╚══════╝╚══════╝" "${white}"
    printf '%b' "${reset}"

    final_banner_points
    final_banner_line

    if [[ "${curl_ok}" == "1" ]]; then
        sleep 0.5
        printf '%b        ... System started successfully. ...%b\n' "${green}" "${reset}"
        printf '\n'
        final_banner_line
    else
        printf '%b%s%b\n' "${yellow}" "Installation completed, but the final HTTP test needs attention." "${reset}"
    fi

    printf '\n'
    printf '%bAccess panel:%b ' "${bold}${white}" "${reset}"
    terminal_link "${url}" "${url}"
    printf '\n'

    printf '%b%s%b\n' "${cyan}" "${sep}" "${reset}"
    printf '%b%-14s%b %s\n' "${bold}" "Panel URL:" "${reset}" "${url}"
    printf '%b%-14s%b %s\n' "${bold}" "Directory:" "${reset}" "${INSTALL_DIR}"
    printf '%b%-14s%b %s\n' "${bold}" "Config:" "${reset}" "${INSTALL_DIR}/public/config.php"
    printf '%b%-14s%b %s\n' "${bold}" "Cron:" "${reset}" "/etc/cron.d/moodle-friendly-installation-runner"
    printf '%b%-14s%b %s\n' "${bold}" "Runner log:" "${reset}" "/var/log/moodle-friendly-installation-runner.log"
    printf '%b%-14s%b %s\n' "${bold}" "Services:" "${reset}" "nginx ${apache_service} ${PHP_FPM_SERVICE}"
    printf '%b%s%b\n' "${cyan}" "${sep}" "${reset}"

    if [[ "${curl_ok}" != "1" ]]; then
        printf '\n%bDiagnostic commands:%b\n' "${yellow}" "${reset}"
        printf '  systemctl status nginx %s %s\n' "${apache_service}" "${PHP_FPM_SERVICE}"
        printf '  journalctl -u nginx -n 80 --no-pager\n'
        printf '  journalctl -u %s -n 80 --no-pager\n' "${apache_service}"
        printf '  journalctl -u %s -n 80 --no-pager\n' "${PHP_FPM_SERVICE}"
        printf '%b%s%b\n' "${cyan}" "${sep}" "${reset}"
    fi

    printf '\n'
}


base_packages_installed() {
    local required_commands=(curl wget git unzip tar python3 sed grep awk openssl)
    local command_name

    for command_name in "${required_commands[@]}"; do
        command_exists "${command_name}" || return 1
    done

    command_exists systemctl || return 1

    return 0
}

php_extensions_ok() {
    local phpbin="$1"
    local modules=""
    local extension=""
    local missing=()

    modules="$(${phpbin} -m 2>/dev/null | tr '[:upper:]' '[:lower:]' || true)"
    [[ -n "${modules}" ]] || return 1

    for extension in curl dom mbstring zip gd intl soap opcache bcmath ldap mysqli pdo_mysql; do
        if [[ "${extension}" == "opcache" ]]; then
            if ! printf '%s\n' "${modules}" | grep -Eqx '(opcache|zend opcache)'; then
                missing+=("${extension}")
            fi
            continue
        fi

        if ! printf '%s\n' "${modules}" | grep -qx "${extension}"; then
            missing+=("${extension}")
        fi
    done

    if (( ${#missing[@]} > 0 )); then
        warn "PHP is installed, but missing required extensions: ${missing[*]}"
        return 1
    fi

    return 0
}

php_requirement_installed() {
    [[ -n "${PHP_REQUIRED:-}" ]] || return 1

    local candidates=()
    local candidate=""
    local phpbin=""
    local php_current=""

    [[ -n "${PHP_BIN:-}" ]] && candidates+=("${PHP_BIN}")
    [[ -n "${PHP_SERIES:-}" ]] && candidates+=("/usr/bin/php${PHP_SERIES}" "php${PHP_SERIES}")
    candidates+=(php)

    for candidate in "${candidates[@]}"; do
        if [[ "${candidate}" == /* ]]; then
            [[ -x "${candidate}" ]] || continue
            phpbin="${candidate}"
        else
            phpbin="$(command -v "${candidate}" 2>/dev/null || true)"
            [[ -n "${phpbin}" ]] || continue
        fi

        php_current="$(${phpbin} -r 'echo PHP_VERSION;' 2>/dev/null || true)"
        [[ -n "${php_current}" ]] || continue
        version_ge "${php_current}" "${PHP_REQUIRED}" || continue
        php_extensions_ok "${phpbin}" || return 1

        PHP_BIN="${phpbin}"
        PHP_SERIES="$(${PHP_BIN} -r 'echo PHP_MAJOR_VERSION.".".PHP_MINOR_VERSION;' 2>/dev/null || printf '%s' "${PHP_SERIES}")"
        PHP_FPM_SERVICE="php-fpm"
        PHP_FPM_SOCKET="/run/php-fpm/www.sock"

        if command_exists systemctl && ! systemctl list-unit-files "${PHP_FPM_SERVICE}.service" >/dev/null 2>&1; then
            warn "PHP CLI satisfies the requirement, but ${PHP_FPM_SERVICE} is not installed. PHP installation will run."
            return 1
        fi

        return 0
    done

    return 1
}

database_requirement_installed() {
    [[ -n "${DB_ENGINE:-}" ]] || return 1

    local client=""
    local raw_version=""
    local version=""
    local required=""
    local label=""

    client="$(mysql_client 2>/dev/null || true)"
    [[ -n "${client}" ]] || return 1
    raw_version="$(${client} --version 2>/dev/null || true)"
    [[ -n "${raw_version}" ]] || return 1

    if [[ "${DB_ENGINE}" == "mariadb" ]]; then
        required="${MARIADB_REQUIRED:-10.11.0}"
        label="MariaDB"
        printf '%s' "${raw_version}" | grep -qi 'mariadb' || return 1
        version="$(printf '%s' "${raw_version}" | grep -oE 'Distrib[[:space:]]+[0-9]+(\.[0-9]+){1,2}' | grep -oE '[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        if [[ -z "${version}" ]]; then
            version="$(printf '%s' "${raw_version}" | grep -oE '[0-9]+(\.[0-9]+){1,2}[^[:space:]]*-MariaDB' | grep -oE '^[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        fi
        if command_exists systemctl && systemctl list-unit-files mariadb.service >/dev/null 2>&1; then
            DB_SERVICE="mariadb"
        elif command_exists systemctl && systemctl list-unit-files mysql.service >/dev/null 2>&1; then
            DB_SERVICE="mysql"
        else
            DB_SERVICE="mariadb"
        fi
    else
        required="${MYSQL_REQUIRED:-8.4}"
        label="MySQL"
        printf '%s' "${raw_version}" | grep -qi 'mariadb' && return 1
        version="$(printf '%s' "${raw_version}" | grep -oE '[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        if command_exists systemctl && systemctl list-unit-files mysql.service >/dev/null 2>&1; then
            DB_SERVICE="mysql"
        elif command_exists systemctl && systemctl list-unit-files mysqld.service >/dev/null 2>&1; then
            DB_SERVICE="mysqld"
        else
            DB_SERVICE="mysql"
        fi
    fi

    [[ -n "${version}" ]] || return 1
    if ! version_ge "${version}" "${required}"; then
        warn "${label} is installed (${version}), but Moodle requires ${required}. Database installation will run."
        return 1
    fi

    return 0
}

web_servers_requirement_installed() {
    command_exists nginx || return 1
    command_exists certbot || return 1

    command_exists httpd || return 1

    return 0
}

lets_encrypt_certificate_exists() {
    [[ -n "${PANEL_DOMAIN:-}" ]] || return 1
    [[ -f "/etc/letsencrypt/live/${PANEL_DOMAIN}/fullchain.pem" ]] || return 1
    [[ -f "/etc/letsencrypt/live/${PANEL_DOMAIN}/privkey.pem" ]] || return 1
    return 0
}

load_existing_panel_config() {
    local config_file="${INSTALL_DIR}/public/config.php"
    local existing_pass=""

    [[ -f "${config_file}" ]] || return 0

    existing_pass="$(python3 - "${config_file}" <<'PY'
import re
import sys
from pathlib import Path

text = Path(sys.argv[1]).read_text(errors='ignore')
match = re.search(r"['\"]mysql_admin_pass['\"]\s*=>\s*('(?:\\.|[^'\\])*'|\"(?:\\.|[^\"\\])*\"|null)", text)
if not match:
    raise SystemExit(0)

token = match.group(1)
if token == 'null':
    raise SystemExit(0)

quote = token[0]
value = token[1:-1]

if quote == "'":
    value = value.replace("\\'", "'").replace('\\\\', '\\')
else:
    # This is intentionally conservative. Installer-generated values are single quoted,
    # but this also supports the most common double-quoted escaping used manually.
    value = bytes(value, 'utf-8').decode('unicode_escape')

if value:
    print(value)
PY
)"

    [[ -n "${existing_pass}" ]] || return 0

    if [[ -n "${REQUESTED_DB_ROOT_PASS:-}" ]]; then
        log "Using database root password provided through DB_ROOT_PASS. Existing panel config was not used."
        return 0
    fi

    if [[ "${DB_ROOT_PASS:-}" != "${existing_pass}" ]]; then
        DB_ROOT_PASS="${existing_pass}"
        log "Loaded database root password from existing panel config.php."
        save_progress
    fi
}

main() {
    need_root
    load_progress
    detect_os
    open_interactive_terminal
    configure_selinux_permissive

    if base_packages_installed; then
        log "Base packages already satisfy the installer requirements. Skipping package bootstrap."
        mark_progress_done STEP_BASE_PACKAGES
    else
        install_base_packages
        mark_progress_done STEP_BASE_PACKAGES
    fi

    load_existing_panel_config
    fetch_moodle_environment
    ask_install_options
    detect_public_ip
    check_dns_before_continue

    if php_requirement_installed; then
        log "PHP already satisfies Moodle requirements. Skipping PHP installation."
        tune_php_ini
        systemctl enable --now "${PHP_FPM_SERVICE}"
        systemctl restart "${PHP_FPM_SERVICE}"
        mark_progress_done STEP_PHP
    else
        install_php
        mark_progress_done STEP_PHP
    fi

    if database_requirement_installed; then
        log "${DB_ENGINE} already satisfies Moodle requirements. Skipping database installation."
        systemctl enable --now "${DB_SERVICE}" || true
        configure_database_root
        validate_database_version
        mark_progress_done STEP_DATABASE
    else
        install_database
        mark_progress_done STEP_DATABASE
    fi

    if web_servers_requirement_installed; then
        log "Apache, NGINX and Certbot are already installed. Skipping web server package installation."
        mark_progress_done STEP_WEB_SERVERS
    else
        install_web_servers
        mark_progress_done STEP_WEB_SERVERS
    fi

    ensure_sites_enabled_includes
    configure_apache_port
    configure_firewall

    install_bundled_nginx_files

    create_panel_config
    write_apache_vhost
    write_nginx_vhost
    set_permissions
    mark_progress_done STEP_CONFIG

    configure_cron
    mark_progress_done STEP_CRON

    restart_services

    if lets_encrypt_certificate_exists; then
        log "Let's Encrypt certificate already exists for ${PANEL_DOMAIN}. Skipping certificate issue."
        mark_progress_done STEP_LETS_ENCRYPT
    else
        issue_lets_encrypt
        mark_progress_done STEP_LETS_ENCRYPT
    fi

    final_check
    mark_progress_done STEP_COMPLETE
}

main "$@"
