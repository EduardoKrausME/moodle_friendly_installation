    if [[ ! -f "${INSTALL_DIR}/public/app/composer.json" ]]; then
        warn "PHP vendor was not found and composer.json does not exist either."
        return 0
    fi

    log "Installing panel PHP dependencies through Composer"
    if ! command_exists composer; then
        pkg_install composer || true
    fi

    command_exists composer || die "Composer is not installed and public/app/vendor/autoload.php does not exist. Install Composer or commit the vendor/ directory."
    (cd "${INSTALL_DIR}/public/app" && composer install --no-dev --optimize-autoloader --no-interaction) || die "composer install failed."
}

update_panel_config_values() {
    local config_file="$1"
    shift

    python3 - "${config_file}" "$@" <<'PY'
import re
import sys
from pathlib import Path

path = Path(sys.argv[1])
updates = []
for raw in sys.argv[2:]:
    key, value = raw.split('=', 1)
    updates.append((key, value))

text = path.read_text(errors='ignore')

for key, value in updates:
    pattern = re.compile(
        r"(?m)^(?P<prefix>[ \t]*['\"]" + re.escape(key) + r"['\"]\s*=>\s*)(?P<value>[^,\n]*)(?P<suffix>,\s*)$"
    )

    def repl(match):
        return f"{match.group('prefix')}{value}{match.group('suffix')}"

    text, count = pattern.subn(repl, text, count=1)
    if count:
        continue

    insert = f"    '{key}' => {value},\n"
    end_match = re.search(r"(?m)^\s*\];\s*$", text)
    if not end_match:
        raise SystemExit(f"Could not update {path}: closing array marker ]; was not found")
    text = text[:end_match.start()] + insert + text[end_match.start():]

path.write_text(text)
PY
}

