php_extensions_ok() {
    local phpbin="$1"
    local modules=""
    local extension=""
    local missing=()

    modules="$(${phpbin} -m 2>/dev/null | tr '[:upper:]' '[:lower:]' || true)"
    [[ -n "${modules}" ]] || return 1

    for extension in curl dom mbstring zip gd intl soap opcache bcmath ldap mysqli pdo_mysql; do
        if [[ "${extension}" == "opcache" ]]; then
            if ! printf '%s\n' "${modules}" | grep -Eqx '(opcache|zend opcache)'; then
                missing+=("${extension}")
            fi
            continue
        fi

        if ! printf '%s\n' "${modules}" | grep -qx "${extension}"; then
            missing+=("${extension}")
        fi
    done

    if (( ${#missing[@]} > 0 )); then
        warn "PHP is installed, but missing required extensions: ${missing[*]}"
        return 1
    fi

    return 0
}

php_requirement_installed() {
    [[ -n "${PHP_REQUIRED:-}" ]] || return 1

    local candidates=()
    local candidate=""
    local phpbin=""
    local php_current=""

    [[ -n "${PHP_BIN:-}" ]] && candidates+=("${PHP_BIN}")
    [[ -n "${PHP_SERIES:-}" ]] && candidates+=("/usr/bin/php${PHP_SERIES}" "php${PHP_SERIES}")
    candidates+=(php)

    for candidate in "${candidates[@]}"; do
        if [[ "${candidate}" == /* ]]; then
            [[ -x "${candidate}" ]] || continue
            phpbin="${candidate}"
        else
            phpbin="$(command -v "${candidate}" 2>/dev/null || true)"
            [[ -n "${phpbin}" ]] || continue
        fi

        php_current="$(${phpbin} -r 'echo PHP_VERSION;' 2>/dev/null || true)"
        [[ -n "${php_current}" ]] || continue
        version_ge "${php_current}" "${PHP_REQUIRED}" || continue
        php_extensions_ok "${phpbin}" || return 1

        PHP_BIN="${phpbin}"
        PHP_SERIES="$(${PHP_BIN} -r 'echo PHP_MAJOR_VERSION.".".PHP_MINOR_VERSION;' 2>/dev/null || printf '%s' "${PHP_SERIES}")"
        PHP_FPM_SERVICE="php${PHP_SERIES}-fpm"
        PHP_FPM_SOCKET="/run/php/php${PHP_SERIES}-fpm.sock"

        if command_exists systemctl && ! systemctl list-unit-files "${PHP_FPM_SERVICE}.service" >/dev/null 2>&1; then
            warn "PHP CLI satisfies the requirement, but ${PHP_FPM_SERVICE} is not installed. PHP installation will run."
            return 1
        fi

        return 0
    done

    return 1
}

database_requirement_installed() {
    [[ -n "${DB_ENGINE:-}" ]] || return 1

    local client=""
    local raw_version=""
    local version=""
    local required=""
    local label=""

    client="$(mysql_client 2>/dev/null || true)"
    [[ -n "${client}" ]] || return 1
    raw_version="$(${client} --version 2>/dev/null || true)"
    [[ -n "${raw_version}" ]] || return 1

    if [[ "${DB_ENGINE}" == "mariadb" ]]; then
        required="${MARIADB_REQUIRED:-10.11.0}"
        label="MariaDB"
        printf '%s' "${raw_version}" | grep -qi 'mariadb' || return 1
        version="$(printf '%s' "${raw_version}" | grep -oE 'Distrib[[:space:]]+[0-9]+(\.[0-9]+){1,2}' | grep -oE '[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        if [[ -z "${version}" ]]; then
            version="$(printf '%s' "${raw_version}" | grep -oE '[0-9]+(\.[0-9]+){1,2}[^[:space:]]*-MariaDB' | grep -oE '^[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        fi
        if command_exists systemctl && systemctl list-unit-files mariadb.service >/dev/null 2>&1; then
            DB_SERVICE="mariadb"
        elif command_exists systemctl && systemctl list-unit-files mysql.service >/dev/null 2>&1; then
            DB_SERVICE="mysql"
        else
            DB_SERVICE="mariadb"
        fi
    else
        required="${MYSQL_REQUIRED:-8.4}"
        label="MySQL"
        printf '%s' "${raw_version}" | grep -qi 'mariadb' && return 1
        version="$(printf '%s' "${raw_version}" | grep -oE '[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        if command_exists systemctl && systemctl list-unit-files mysql.service >/dev/null 2>&1; then
            DB_SERVICE="mysql"
        elif command_exists systemctl && systemctl list-unit-files mysqld.service >/dev/null 2>&1; then
            DB_SERVICE="mysqld"
        else
            DB_SERVICE="mysql"
        fi
    fi

    [[ -n "${version}" ]] || return 1
    if ! version_ge "${version}" "${required}"; then
        warn "${label} is installed (${version}), but Moodle requires ${required}. Database installation will run."
        return 1
    fi

    return 0
}

