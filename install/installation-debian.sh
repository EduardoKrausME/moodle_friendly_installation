        printf 'MOODLE_REQUIRES=%q\n' "${MOODLE_REQUIRES:-}"
        printf 'MOODLE_STABLE_BRANCH=%q\n' "${MOODLE_STABLE_BRANCH:-}"
        printf 'PHP_REQUIRED=%q\n' "${PHP_REQUIRED:-}"
        printf 'PHP_SERIES=%q\n' "${PHP_SERIES:-}"
        printf 'PHP_BIN=%q\n' "${PHP_BIN:-}"
        printf 'PHP_FPM_SERVICE=%q\n' "${PHP_FPM_SERVICE:-}"
        printf 'PHP_FPM_SOCKET=%q\n' "${PHP_FPM_SOCKET:-}"
        printf 'MARIADB_REQUIRED=%q\n' "${MARIADB_REQUIRED:-}"
