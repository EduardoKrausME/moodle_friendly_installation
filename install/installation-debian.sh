remove_legacy_panel_vhost() {
    local dir="$1"
    local file="$2"
    local target_file="${dir}/${PANEL_VHOST_FILE}"

    [[ -n "${file}" ]] || return 0
    [[ "${file}" != "${target_file}" ]] || return 0
    [[ -f "${file}" ]] || return 0

    if grep -Eq "DocumentRoot[[:space:]]+${INSTALL_DIR}/public|moodle-friendly-installation|proxy_pass[[:space:]]+http://127\\.0\\.0\\.1:8080" "${file}" 2>/dev/null; then
        rm -f "${file}"
    fi
}

cleanup_legacy_panel_vhosts() {
    local dir="$1"
    local current_file="${dir}/${PANEL_VHOST_FILE}"
    local legacy_file

    for legacy_file in "${dir}/${SERVER_NAME}.conf" "${dir}/_.conf"; do
        remove_legacy_panel_vhost "${dir}" "${legacy_file}"
    done

    if [[ -d "${dir}" ]]; then
        while IFS= read -r legacy_file; do
            [[ "${legacy_file}" != "${current_file}" ]] || continue
            remove_legacy_panel_vhost "${dir}" "${legacy_file}"
        done < <(grep -RlE "DocumentRoot[[:space:]]+${INSTALL_DIR}/public|moodle-friendly-installation|proxy_pass[[:space:]]+http://127\\.0\\.0\\.1:8080" "${dir}"/*.conf 2>/dev/null || true)
    fi
}

write_apache_vhost() {
    APACHE_SITES_DIR="$(resolve_apache_sites_dir)"
    local file="${APACHE_SITES_DIR}/${PANEL_VHOST_FILE}"
    cleanup_legacy_panel_vhosts "${APACHE_SITES_DIR}"
    local server_for_apache="${PANEL_DOMAIN:-${PUBLIC_IP}}"
    local apache_log_dir="/var/log/httpd"
    if [[ "${OS_FAMILY}" == "debian" ]]; then
        apache_log_dir="/var/log/apache2"
    fi
    cat > "${file}" <<APACHE
<VirtualHost 127.0.0.1:8080>
    ServerName ${server_for_apache}
    DocumentRoot ${INSTALL_DIR}/public

    <Directory ${INSTALL_DIR}>
        Require all denied
    </Directory>

    <Directory ${INSTALL_DIR}/public>
        Options FollowSymLinks
        AllowOverride All
        Require all granted
    </Directory>

    <FilesMatch \.php$>
        SetHandler "proxy:unix:${PHP_FPM_SOCKET}|fcgi://localhost/"
    </FilesMatch>

    ErrorLog ${apache_log_dir}/moodle-friendly-installation-error.log
    CustomLog ${apache_log_dir}/moodle-friendly-installation-access.log combined
</VirtualHost>
APACHE
}

write_nginx_vhost() {
    local file="${NGINX_SITES_DIR}/${PANEL_VHOST_FILE}"
    cleanup_legacy_panel_vhosts "${NGINX_SITES_DIR}"
    local listen="listen 80;"
    local server_name="${PANEL_DOMAIN:-_}"
    if [[ -z "${PANEL_DOMAIN}" ]]; then
        listen="listen 80 default_server;"
    fi

    rm -f /etc/nginx/sites-enabled/default /etc/nginx/conf.d/default.conf 2>/dev/null || true

    cat > "${file}" <<NGINX
server {
    ${listen}
    server_name ${server_name};

    client_max_body_size 128m;

    location / {
        proxy_set_header Host \$host;
        proxy_set_header X-Real-IP \$remote_addr;
        proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto \$scheme;
        proxy_set_header X-Forwarded-Host \$host;
        proxy_read_timeout 300;
        proxy_connect_timeout 300;
        proxy_pass http://127.0.0.1:8080;
    }
}
NGINX
}

set_permissions() {
    log "Adjusting permissions"
    chown -R root:"${WEB_GROUP}" "${INSTALL_DIR}"
    find "${INSTALL_DIR}" -type d -exec chmod 0750 {} \;
    find "${INSTALL_DIR}" -type f -exec chmod 0640 {} \;
    chmod +x "${INSTALL_DIR}/bin/cron-root-runner.php" "${INSTALL_DIR}/bin/cron-install_moodle.php" "${INSTALL_DIR}/bin/cron-app_build.php" 2>/dev/null || true
    chmod 0770 "${INSTALL_DIR}/data" "${INSTALL_DIR}/runtime" "${INSTALL_DIR}/logs" "${INSTALL_DIR}/queue"
    chmod 0640 "${INSTALL_DIR}/public/config.php" "${INSTALL_DIR}/data/users.json"

    if [[ "${OS_FAMILY}" != "debian" ]] && command_exists restorecon; then
        if command_exists semanage; then
            semanage fcontext -a -t httpd_sys_content_t "${INSTALL_DIR}/public(/.*)?" >/dev/null 2>&1 || true
            semanage fcontext -a -t httpd_sys_rw_content_t "${INSTALL_DIR}/(data|runtime|logs|queue)(/.*)?" >/dev/null 2>&1 || true
        fi
        restorecon -R "${INSTALL_DIR}" >/dev/null 2>&1 || true
    fi
}

configure_cron() {
    log "Configuring the panel root cron"
    cat > /etc/cron.d/moodle-friendly-installation-runner <<CRON
* * * * * root ${PHP_BIN} ${INSTALL_DIR}/bin/cron-root-runner.php >>/var/log/moodle-friendly-installation-runner.log 2>&1
CRON
    chmod 0644 /etc/cron.d/moodle-friendly-installation-runner
}

restart_services() {
    log "Validating and restarting services"
    systemctl enable --now "${PHP_FPM_SERVICE}"
    systemctl restart "${PHP_FPM_SERVICE}"

    systemctl enable --now "${APACHE_SERVICE}"
    systemctl restart "${APACHE_SERVICE}"

    systemctl enable --now nginx

    if [[ "${OS_FAMILY}" == "debian" ]]; then
        apache2ctl configtest
    else
        httpd -t
    fi
    nginx -t

    systemctl restart nginx
}

issue_lets_encrypt() {
    [[ -n "${PANEL_DOMAIN}" ]] || return 0

    prompt_text LE_EMAIL "E-mail para Let's Encrypt" "admin@${PANEL_DOMAIN}"
    [[ "${LE_EMAIL}" =~ ^[^@[:space:]]+@[^@[:space:]]+\.[^@[:space:]]+$ ]] || die "Invalid Let's Encrypt email."

    log "Issuing Let's Encrypt certificate for ${PANEL_DOMAIN}"
    certbot --nginx -d "${PANEL_DOMAIN}" --non-interactive --agree-tos -m "${LE_EMAIL}" --redirect || die "Failed to issue the Let's Encrypt certificate. Check DNS, ports 80/443 and firewall."
    systemctl reload nginx
}

final_check() {
    local url="${BASE_URL}"
    log "Final test: ${url}"
    if curl -k -fsSI --max-time 15 "${url}" >/dev/null 2>&1; then
        log "Panel is responding at ${url}"
    else
        warn "Could not validate through curl. Check: systemctl status nginx ${APACHE_SERVICE} ${PHP_FPM_SERVICE}"
    fi

    cat <<EOF

Installation completed.
Panel URL: ${url}
Directory: ${INSTALL_DIR}
Config: ${INSTALL_DIR}/public/config.php
Cron: /etc/cron.d/moodle-friendly-installation-runner
Runner log: /var/log/moodle-friendly-installation-runner.log

EOF
}


base_packages_installed() {
    local required_commands=(curl wget git unzip tar python3 sed grep awk openssl)
    local command_name

    for command_name in "${required_commands[@]}"; do
        command_exists "${command_name}" || return 1
    done

    if [[ "${OS_FAMILY}" != "debian" ]]; then
        command_exists systemctl || return 1
    fi

    return 0
}

php_extensions_ok() {
    local phpbin="$1"
    local modules=""
    local extension=""
    local missing=()

    modules="$(${phpbin} -m 2>/dev/null | tr '[:upper:]' '[:lower:]' || true)"
    [[ -n "${modules}" ]] || return 1

    for extension in curl dom mbstring zip gd intl soap opcache bcmath ldap mysqli pdo_mysql; do
        if [[ "${extension}" == "opcache" ]]; then
            if ! printf '%s\n' "${modules}" | grep -Eqx '(opcache|zend opcache)'; then
                missing+=("${extension}")
            fi
            continue
        fi

        if ! printf '%s\n' "${modules}" | grep -qx "${extension}"; then
            missing+=("${extension}")
        fi
    done

    if (( ${#missing[@]} > 0 )); then
        warn "PHP is installed, but missing required extensions: ${missing[*]}"
        return 1
    fi

    return 0
}

php_requirement_installed() {
    [[ -n "${PHP_REQUIRED:-}" ]] || return 1

    local candidates=()
    local candidate=""
    local phpbin=""
    local php_current=""

    [[ -n "${PHP_BIN:-}" ]] && candidates+=("${PHP_BIN}")
    [[ -n "${PHP_SERIES:-}" ]] && candidates+=("/usr/bin/php${PHP_SERIES}" "php${PHP_SERIES}")
    candidates+=(php)

    for candidate in "${candidates[@]}"; do
        if [[ "${candidate}" == /* ]]; then
            [[ -x "${candidate}" ]] || continue
            phpbin="${candidate}"
        else
            phpbin="$(command -v "${candidate}" 2>/dev/null || true)"
            [[ -n "${phpbin}" ]] || continue
        fi

        php_current="$(${phpbin} -r 'echo PHP_VERSION;' 2>/dev/null || true)"
        [[ -n "${php_current}" ]] || continue
        version_ge "${php_current}" "${PHP_REQUIRED}" || continue
        php_extensions_ok "${phpbin}" || return 1

        PHP_BIN="${phpbin}"
        PHP_SERIES="$(${PHP_BIN} -r 'echo PHP_MAJOR_VERSION.".".PHP_MINOR_VERSION;' 2>/dev/null || printf '%s' "${PHP_SERIES}")"
        if [[ "${OS_FAMILY}" == "debian" ]]; then
            PHP_FPM_SERVICE="php${PHP_SERIES}-fpm"
            PHP_FPM_SOCKET="/run/php/php${PHP_SERIES}-fpm.sock"
        else
            PHP_FPM_SERVICE="php-fpm"
            PHP_FPM_SOCKET="/run/php-fpm/www.sock"
        fi

        if command_exists systemctl && ! systemctl list-unit-files "${PHP_FPM_SERVICE}.service" >/dev/null 2>&1; then
            warn "PHP CLI satisfies the requirement, but ${PHP_FPM_SERVICE} is not installed. PHP installation will run."
            return 1
        fi

        return 0
    done

    return 1
}

database_requirement_installed() {
    [[ -n "${DB_ENGINE:-}" ]] || return 1

    local client=""
    local raw_version=""
    local version=""
    local required=""
    local label=""

    client="$(mysql_client 2>/dev/null || true)"
    [[ -n "${client}" ]] || return 1
    raw_version="$(${client} --version 2>/dev/null || true)"
    [[ -n "${raw_version}" ]] || return 1

    if [[ "${DB_ENGINE}" == "mariadb" ]]; then
        required="${MARIADB_REQUIRED:-10.11.0}"
        label="MariaDB"
        printf '%s' "${raw_version}" | grep -qi 'mariadb' || return 1
        version="$(printf '%s' "${raw_version}" | grep -oE 'Distrib[[:space:]]+[0-9]+(\.[0-9]+){1,2}' | grep -oE '[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        if [[ -z "${version}" ]]; then
            version="$(printf '%s' "${raw_version}" | grep -oE '[0-9]+(\.[0-9]+){1,2}[^[:space:]]*-MariaDB' | grep -oE '^[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        fi
        if command_exists systemctl && systemctl list-unit-files mariadb.service >/dev/null 2>&1; then
            DB_SERVICE="mariadb"
        elif command_exists systemctl && systemctl list-unit-files mysql.service >/dev/null 2>&1; then
            DB_SERVICE="mysql"
        else
            DB_SERVICE="mariadb"
        fi
    else
        required="${MYSQL_REQUIRED:-8.4}"
        label="MySQL"
        printf '%s' "${raw_version}" | grep -qi 'mariadb' && return 1
        version="$(printf '%s' "${raw_version}" | grep -oE '[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        if command_exists systemctl && systemctl list-unit-files mysql.service >/dev/null 2>&1; then
            DB_SERVICE="mysql"
        elif command_exists systemctl && systemctl list-unit-files mysqld.service >/dev/null 2>&1; then
            DB_SERVICE="mysqld"
        else
            DB_SERVICE="mysql"
        fi
    fi

    [[ -n "${version}" ]] || return 1
    if ! version_ge "${version}" "${required}"; then
        warn "${label} is installed (${version}), but Moodle requires ${required}. Database installation will run."
        return 1
    fi

    return 0
}

web_servers_requirement_installed() {
    command_exists nginx || return 1
    command_exists certbot || return 1

    if [[ "${OS_FAMILY}" == "debian" ]]; then
        command_exists apache2ctl || return 1
    else
        command_exists httpd || return 1
    fi

    return 0
}

lets_encrypt_certificate_exists() {
    [[ -n "${PANEL_DOMAIN:-}" ]] || return 1
    [[ -f "/etc/letsencrypt/live/${PANEL_DOMAIN}/fullchain.pem" ]] || return 1
    [[ -f "/etc/letsencrypt/live/${PANEL_DOMAIN}/privkey.pem" ]] || return 1
    return 0
}

main() {
    need_root
    load_progress
    detect_os
    open_interactive_terminal

    if base_packages_installed; then
        log "Base packages already satisfy the installer requirements. Skipping package bootstrap."
        mark_progress_done STEP_BASE_PACKAGES
    else
        install_base_packages
        mark_progress_done STEP_BASE_PACKAGES
    fi

    load_existing_panel_config
    fetch_moodle_environment
    ask_install_options
    detect_public_ip
    check_dns_before_continue

    if php_requirement_installed; then
        log "PHP already satisfies Moodle requirements. Skipping PHP installation."
        tune_php_ini
        systemctl enable --now "${PHP_FPM_SERVICE}"
        systemctl restart "${PHP_FPM_SERVICE}"
        mark_progress_done STEP_PHP
    else
        install_php
        mark_progress_done STEP_PHP
    fi

    if database_requirement_installed; then
        log "${DB_ENGINE} already satisfies Moodle requirements. Skipping database installation."
        systemctl enable --now "${DB_SERVICE}" || true
        configure_database_root
        validate_database_version
        mark_progress_done STEP_DATABASE
    else
        install_database
        mark_progress_done STEP_DATABASE
    fi

    if web_servers_requirement_installed; then
        log "Apache, NGINX and Certbot are already installed. Skipping web server package installation."
        mark_progress_done STEP_WEB_SERVERS
    else
        install_web_servers
        mark_progress_done STEP_WEB_SERVERS
    fi

    ensure_sites_enabled_includes
    configure_apache_port
    configure_firewall

    install_bundled_nginx_files

    create_panel_config
    write_apache_vhost
    write_nginx_vhost
    set_permissions
    mark_progress_done STEP_CONFIG

    configure_cron
    mark_progress_done STEP_CRON

    restart_services

    if lets_encrypt_certificate_exists; then
        log "Let's Encrypt certificate already exists for ${PANEL_DOMAIN}. Skipping certificate issue."
        mark_progress_done STEP_LETS_ENCRYPT
    else
        issue_lets_encrypt
        mark_progress_done STEP_LETS_ENCRYPT
    fi

    final_check
    mark_progress_done STEP_COMPLETE
}

main "$@"
