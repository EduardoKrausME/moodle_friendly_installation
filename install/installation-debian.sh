install_mariadb() {
    log "Installing MariaDB >= ${MARIADB_REQUIRED:-10.11}"
    local wanted="${MARIADB_REQUIRED:-10.11.0}"
    local series="$(printf '%s' "${wanted}" | awk -F. '{print $1"."$2}')"

    # Prefer the distribution repository first. Debian 12/13 and Ubuntu 24.04 already
    # provide a MariaDB version compatible with the current Moodle requirement.
    if [[ "${OS_FAMILY}" == "debian" ]]; then
        pkg_install mariadb-server mariadb-client
    else
        pkg_install MariaDB-server MariaDB-client || pkg_install mariadb-server mariadb
    fi

    DB_SERVICE="mariadb"
    systemctl enable --now mariadb || systemctl enable --now mysql

    local client raw_version version=""
    client="$(mysql_client || true)"
    if [[ -n "${client}" ]]; then
        raw_version="$(${client} --version || true)"
        version="$(printf '%s' "${raw_version}" | grep -oE 'Distrib[[:space:]]+[0-9]+(\.[0-9]+){1,2}' | grep -oE '[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        if [[ -z "${version}" ]]; then
            version="$(printf '%s' "${raw_version}" | grep -oE '[0-9]+(\.[0-9]+){1,2}[^[:space:]]*-MariaDB' | grep -oE '^[0-9]+(\.[0-9]+){1,2}' | head -n1 || true)"
        fi
    fi

    if [[ -n "${version}" ]] && version_ge "${version}" "${wanted}"; then
        return 0
    fi

    warn "Distribution MariaDB (${version:-unknown}) is lower than required (${wanted}). Trying the MariaDB upstream repository."
    if curl -fsSL https://r.mariadb.com/downloads/mariadb_repo_setup -o /tmp/mariadb_repo_setup; then
        chmod +x /tmp/mariadb_repo_setup
        bash /tmp/mariadb_repo_setup --mariadb-server-version="mariadb-${series}" || die "Failed to configure MariaDB ${series} repository."
        pkg_update
        if [[ "${OS_FAMILY}" == "debian" ]]; then
            pkg_install mariadb-server mariadb-client
        else
            pkg_install MariaDB-server MariaDB-client || pkg_install mariadb-server mariadb
        fi
        systemctl enable --now mariadb || systemctl enable --now mysql
    else
        die "Could not download mariadb_repo_setup and the distribution MariaDB is not compatible."
    fi
}

setup_mysql_apt_repository() {
    # Moodle 5.2+ currently requires MySQL 8.4. Ubuntu LTS repositories may still provide MySQL 8.0,
    # so use Oracle's APT repository when the user chooses MySQL.
    [[ "${OS_FAMILY}" == "debian" ]] || return 0

    local codename=""
    codename="$(lsb_release -sc 2>/dev/null || true)"
    if [[ -z "${codename}" && -r /etc/os-release ]]; then
        # shellcheck source=/dev/null
        source /etc/os-release
        codename="${VERSION_CODENAME:-}"
    fi
    [[ -n "${codename}" ]] || die "Could not detect the Debian/Ubuntu codename for the MySQL APT repository."

    local apt_os="debian"
    if [[ "${OS_ID}" == "ubuntu" ]]; then
        apt_os="ubuntu"
    fi

    log "Configuring the official MySQL APT repository for MySQL ${MYSQL_REQUIRED:-8.4}"
    mkdir -p /usr/share/keyrings
    # Oracle rotated the MySQL repository signing key after the 2023 key expired.
    # Always prefer the newest published key and keep older keys only as fallback.
    local key_tmp="/tmp/mysql-gpg-key.asc"
    local key_url=""
    for key_url in \
        https://repo.mysql.com/RPM-GPG-KEY-mysql-2025 \
        https://repo.mysql.com/RPM-GPG-KEY-mysql-2023 \
        https://repo.mysql.com/RPM-GPG-KEY-mysql-2022; do
        if curl -fsSL "${key_url}" -o "${key_tmp}"; then
            break
        fi
    done
    [[ -s "${key_tmp}" ]] || die "Could not download the MySQL repository GPG key."
    gpg --dearmor < "${key_tmp}" > /usr/share/keyrings/mysql.gpg
    chmod 0644 /usr/share/keyrings/mysql.gpg

    # Remove stale MySQL APT repository files from previous runs.
    # A stale file signed by the expired 2023 key can break apt-get update even after
    # this installer writes a new signed-by repository file.
    find /etc/apt/sources.list.d -maxdepth 1 -type f \
        \( -name 'mysql*.list' -o -name '*mysql*.list' -o -name 'mysql*.sources' -o -name '*mysql*.sources' \) \
        -print -delete 2>/dev/null || true

    cat > /etc/apt/sources.list.d/mysql-community.list <<MYSQLAPT
# Added by Moodle Friendly Installation installer.
deb [signed-by=/usr/share/keyrings/mysql.gpg] https://repo.mysql.com/apt/${apt_os}/ ${codename} mysql-8.4-lts mysql-tools
MYSQLAPT
    apt-get update -y || die "Failed to update APT metadata after enabling the MySQL repository. The most common cause is an expired Oracle MySQL repository signing key. This installer uses RPM-GPG-KEY-mysql-2025 first; remove any old repo.mysql.com entries and run again if the error persists."
}

setup_mysql_yum_repository() {
    [[ "${OS_FAMILY}" == "rhel" || "${OS_FAMILY}" == "fedora" ]] || return 0

    local major="${OS_VERSION_ID%%.*}"
    local platform=""
    if [[ "${OS_FAMILY}" == "fedora" ]]; then
        platform="fc${major}"
    else
        platform="el${major}"
        ${PKG_MANAGER} -y module disable mysql >/dev/null 2>&1 || true
    fi

    log "Configuring the official MySQL Yum/DNF repository for MySQL ${MYSQL_REQUIRED:-8.4}"
    pkg_install "https://repo.mysql.com/mysql84-community-release-${platform}-1.noarch.rpm" || warn "Could not install the MySQL repository RPM for ${platform}; trying the distribution repository."

    # Import the newest Oracle MySQL signing key. Some repository RPMs still point
    # to older keys, and the 2023 key expired in 2025.
    if command_exists rpm; then
        rpm --import https://repo.mysql.com/RPM-GPG-KEY-mysql-2025 ||             rpm --import https://repo.mysql.com/RPM-GPG-KEY-mysql-2023 ||             warn "Could not import the latest MySQL RPM GPG key."
    fi
    if [[ -d /etc/yum.repos.d ]]; then
        sed -i 's#^gpgkey=.*RPM-GPG-KEY-mysql-[0-9][0-9][0-9][0-9]#gpgkey=https://repo.mysql.com/RPM-GPG-KEY-mysql-2025#' /etc/yum.repos.d/mysql*.repo 2>/dev/null || true
    fi
}

install_mysql() {
    log "Installing MySQL >= ${MYSQL_REQUIRED:-8.4}"

    if [[ "${OS_FAMILY}" == "debian" ]]; then
        setup_mysql_apt_repository
        if command_exists debconf-set-selections; then
            printf 'mysql-community-server mysql-community-server/root-pass password %s\n' "${DB_ROOT_PASS}" | debconf-set-selections || true
            printf 'mysql-community-server mysql-community-server/re-root-pass password %s\n' "${DB_ROOT_PASS}" | debconf-set-selections || true
