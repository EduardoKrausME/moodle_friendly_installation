log() {
    printf '\n\n\n\033[1;32m==>\033[0m %s\n' "$*"
}

warn() {
    printf '\n\n\n\033[1;33mWARN:\033[0m %s\n' "$*" >&2
}

die() {
    printf '\n\n\n\033[1;31mERROR:\033[0m %s\n' "$*" >&2
    exit 1
}

need_root() {
    if [[ "${EUID}" -ne 0 ]]; then
        die "Run as root: sudo bash $0"
    fi
}

command_exists() {
    command -v "$1" >/dev/null 2>&1
}

version_ge() {
    # Returns true when $1 >= $2.
    [[ "$(printf '%s\n%s\n' "$2" "$1" | sort -V | head -n1)" == "$2" ]]
}

safe_php_string() {
    python3 - "$1" <<'PY'
import sys
s = sys.argv[1]
s = s.replace('\\', '\\\\').replace("'", "\\'")
print("'" + s + "'")
PY
}

ensure_progress_dir() {
    mkdir -p "${PROGRESS_DIR}"
    chmod 0700 "${PROGRESS_DIR}" 2>/dev/null || true
}

save_progress() {
    ensure_progress_dir

    local tmpfile="${PROGRESS_FILE}.tmp"
    {
        printf '# Generated by Moodle Friendly Installation installer.\n'
        printf '# This file allows the installer to resume safely after an interruption.\n'
        printf 'DB_ENGINE=%q\n' "${DB_ENGINE:-}"
        printf 'PANEL_DOMAIN=%q\n' "${PANEL_DOMAIN:-}"
