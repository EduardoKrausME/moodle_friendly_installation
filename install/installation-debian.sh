load_progress() {
    if [[ "${RESET_INSTALLER_PROGRESS:-0}" == "1" ]]; then
        rm -f "${PROGRESS_FILE}" 2>/dev/null || true
        return 0
    fi

    [[ -f "${PROGRESS_FILE}" ]] || return 0

    # shellcheck source=/dev/null
    source "${PROGRESS_FILE}"

    # Explicit environment variables win over saved progress.
    [[ -n "${REQUESTED_DB_ENGINE:-}" ]] && DB_ENGINE="${REQUESTED_DB_ENGINE}"
    [[ -n "${REQUESTED_PANEL_DOMAIN:-}" ]] && PANEL_DOMAIN="${REQUESTED_PANEL_DOMAIN}"
    [[ -n "${REQUESTED_DB_ROOT_PASS:-}" ]] && DB_ROOT_PASS="${REQUESTED_DB_ROOT_PASS}"
    [[ -n "${REQUESTED_LE_EMAIL:-}" ]] && LE_EMAIL="${REQUESTED_LE_EMAIL}"

    log "Loaded installer progress from ${PROGRESS_FILE}"
}

load_existing_panel_config() {
    local config_file="${INSTALL_DIR}/public/config.php"
    local existing_pass=""

    [[ -f "${config_file}" ]] || return 0

    existing_pass="$(python3 - "${config_file}" <<'PY'
import re
import sys
from pathlib import Path

text = Path(sys.argv[1]).read_text(errors='ignore')
match = re.search(r"['\"]mysql_admin_pass['\"]\s*=>\s*('(?:\\.|[^'\\])*'|\"(?:\\.|[^\"\\])*\"|null)", text)
if not match:
    raise SystemExit(0)

token = match.group(1)
if token == 'null':
    raise SystemExit(0)

quote = token[0]
value = token[1:-1]

if quote == "'":
    value = value.replace("\\'", "'").replace('\\\\', '\\')
else:
    # This is intentionally conservative. Installer-generated values are single quoted,
    # but this also supports the most common double-quoted escaping used manually.
    value = bytes(value, 'utf-8').decode('unicode_escape')

if value:
    print(value)
PY
)"

    [[ -n "${existing_pass}" ]] || return 0

    if [[ -n "${REQUESTED_DB_ROOT_PASS:-}" ]]; then
        log "Using database root password provided through DB_ROOT_PASS. Existing panel config was not used."
        return 0
    fi

    if [[ "${DB_ROOT_PASS:-}" != "${existing_pass}" ]]; then
        DB_ROOT_PASS="${existing_pass}"
        log "Loaded database root password from existing panel config.php."
        save_progress
    fi
}

mark_progress_done() {
    local var_name="$1"
    printf -v "${var_name}" '%s' "1"
    save_progress
}

open_interactive_terminal() {
    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        return 0
    fi

    if [[ ! -e /dev/tty ]]; then
        die "Interactive installation requires a TTY. Run from SSH/terminal, or use NONINTERACTIVE=1 with DB_ENGINE and PANEL_DOMAIN."
    fi

    # Keep a stable terminal handle. This avoids prompts disappearing or read blocking
    # when the installer itself was started through curl | sudo bash.
    exec 3</dev/tty || die "Could not open /dev/tty for reading."
    exec 4>/dev/tty || die "Could not open /dev/tty for writing."
    TTY_IN_FD_OPENED=1
}

prompt_text() {
    local var_name="$1"
    local label="$2"
    local default_value="${3:-}"
    local value=""

    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        value="${!var_name:-${default_value}}"
    else
        [[ "${TTY_IN_FD_OPENED}" == "1" ]] || open_interactive_terminal

        if [[ -n "${default_value}" ]]; then
            printf '%s [%s]: ' "${label}" "${default_value}" >&4
            if ! IFS= read -r value <&3; then
                die "Could not read input from the terminal."
            fi
            value="${value:-${default_value}}"
        else
            printf '%s: ' "${label}" >&4
            if ! IFS= read -r value <&3; then
                die "Could not read input from the terminal."
            fi
        fi
    fi

    printf -v "${var_name}" '%s' "${value}"
}

prompt_secret() {
    local var_name="$1"
    local label="$2"
    local value=""

    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        value="${!var_name:-}"
        [[ -n "${value}" ]] || die "Variable ${var_name} must be defined when NONINTERACTIVE=1."
    else
        [[ "${TTY_IN_FD_OPENED}" == "1" ]] || open_interactive_terminal
        printf '%s: ' "${label}" >&4
        if ! IFS= read -r -s value <&3; then
            printf '
' >&4
            die "Could not read password from the terminal."
        fi
        printf '
' >&4
    fi

    printf -v "${var_name}" '%s' "${value}"
}

draw_box_line() {
    local width="${1:-78}"
    printf '+%*s+\n' "$((width - 2))" '' | tr ' ' '-' >&4
}

box_message() {
    local title="$1"
    shift
    local width=78
    [[ "${TTY_IN_FD_OPENED}" == "1" ]] || open_interactive_terminal

    printf '\n' >&4
    draw_box_line "${width}"
    printf '| %-*s |\n' "$((width - 4))" "${title}" >&4
    draw_box_line "${width}"
    local line
    for line in "$@"; do
        printf '| %-*s |\n' "$((width - 4))" "${line}" >&4
    done
    draw_box_line "${width}"
}

prompt_database_select() {
    if [[ -n "${DB_ENGINE:-}" ]]; then
        DB_ENGINE="$(printf '%s' "${DB_ENGINE}" | tr '[:upper:]' '[:lower:]')"
        case "${DB_ENGINE}" in
            mariadb|mysql)
                log "Using saved database option: ${DB_ENGINE}"
                save_progress
                return 0
                ;;
            *)
                die "Invalid saved database option: ${DB_ENGINE}. Use mariadb or mysql."
                ;;
        esac
    fi

    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        DB_ENGINE="${DB_ENGINE:-mariadb}"
        DB_ENGINE="$(printf '%s' "${DB_ENGINE}" | tr '[:upper:]' '[:lower:]')"
    else
        [[ "${TTY_IN_FD_OPENED}" == "1" ]] || open_interactive_terminal

        local choice=""
        while true; do
            box_message \
                "Database to install" \
                "1) MariaDB ${MARIADB_REQUIRED:+>= ${MARIADB_REQUIRED}}" \
                "2) MySQL ${MYSQL_REQUIRED:+>= ${MYSQL_REQUIRED}}" \
                "" \
                "Type 1 or 2 and press ENTER. Default: 1"
            printf 'Select database [1]: ' >&4
            if ! IFS= read -r choice <&3; then
                die "Could not read input from the terminal."
            fi
            choice="${choice:-1}"

            case "${choice}" in
                1|mariadb|MariaDB|MARIADB)
                    DB_ENGINE="mariadb"
                    break
                    ;;
                2|mysql|MySQL|MYSQL)
                    DB_ENGINE="mysql"
                    break
                    ;;
                *)
                    printf 'Invalid option. Type 1 for MariaDB or 2 for MySQL.\n' >&4
                    ;;
            esac
        done
    fi

    case "${DB_ENGINE}" in
        mariadb|mysql) ;;
        *) die "Invalid database option: ${DB_ENGINE}. Use mariadb or mysql." ;;
    esac

    save_progress
}

prompt_domain_box() {
    if [[ "${PANEL_DOMAIN_SELECTED:-0}" == "1" && "${NONINTERACTIVE}" != "1" ]]; then
        if [[ -n "${PANEL_DOMAIN:-}" ]]; then
            log "Using saved panel access option: ${PANEL_DOMAIN}"
        else
            log "Using saved panel access option: public IP"
        fi
        return 0
    fi

    if [[ "${NONINTERACTIVE}" == "1" ]]; then
        PANEL_DOMAIN="${PANEL_DOMAIN:-}"
    else
        [[ "${TTY_IN_FD_OPENED}" == "1" ]] || open_interactive_terminal
        local value=""
        box_message \
            "Panel domain" \
            "Type the domain that will be used by the panel." \
            "Leave blank to install using the public IP of this server." \
            "" \
            "Examples: panel.example.com or empty for IP mode"
        printf 'Panel domain []: ' >&4
        if ! IFS= read -r value <&3; then
            die "Could not read input from the terminal."
        fi
        PANEL_DOMAIN="${value}"
    fi

    PANEL_DOMAIN="$(printf '%s' "${PANEL_DOMAIN}" | tr '[:upper:]' '[:lower:]' | sed -E 's#^https?://##; s#/.*$##; s/:.*$//')"
    PANEL_DOMAIN_SELECTED=1
    save_progress
}

generate_strong_password() {
    python3 - <<'GENPASS'
import random
import secrets

lower = "abcdefghijkmnopqrstuvwxyz"
upper = "ABCDEFGHJKLMNPQRSTUVWXYZ"
digits = "23456789"
symbols = "@#%_-+="
alphabet = lower + upper + digits + symbols
password = [
    secrets.choice(lower),
    secrets.choice(upper),
    secrets.choice(digits),
    secrets.choice(symbols),
]
password.extend(secrets.choice(alphabet) for _ in range(28))
random.SystemRandom().shuffle(password)
print("".join(password))
GENPASS
}

resolve_apache_sites_dir() {
    case "${OS_FAMILY}" in
        debian)
            printf '%s\n' "${DEBIAN_APACHE_SITES_DIR}"
            ;;
        fedora|rhel)
            printf '%s\n' "${REDHAT_APACHE_SITES_DIR}"
            ;;
        *)
            printf '%s\n' "${APACHE_SITES_DIR:-${DEBIAN_APACHE_SITES_DIR}}"
            ;;
    esac
}

detect_os() {
    [[ -r /etc/os-release ]] || die "Could not detect the Linux distribution."
    # shellcheck source=/dev/null
    source /etc/os-release
    OS_ID="${ID:-}"
    OS_VERSION_ID="${VERSION_ID:-}"
    OS_LIKE="${ID_LIKE:-}"

    case "${OS_ID} ${OS_LIKE}" in
        *debian*|*ubuntu*) ;;
        *)
            die "This installer supports the Debian/Ubuntu family only. Detected: ID=${OS_ID}, ID_LIKE=${OS_LIKE}. Use install/installation.sh to auto-select the correct installer."
            ;;
    esac

    OS_FAMILY="debian"
    PKG_MANAGER="apt"
    WEB_USER="www-data"
    WEB_GROUP="www-data"
    APACHE_SERVICE="apache2"
    APACHE_SITES_DIR="${DEBIAN_APACHE_SITES_DIR}"

    log "Detected system: ${OS_ID} ${OS_VERSION_ID} (${OS_FAMILY})"
}
pkg_update() {
    if [[ "${OS_FAMILY}" == "debian" ]]; then
        export DEBIAN_FRONTEND=noninteractive
        apt-get update -y
    else
        ${PKG_MANAGER} makecache -y || true
    fi
}

pkg_install() {
    if [[ "${OS_FAMILY}" == "debian" ]]; then
        export DEBIAN_FRONTEND=noninteractive
        apt-get install -y "$@"
    else
        ${PKG_MANAGER} install -y "$@"
    fi
}

install_base_packages() {
    log "Installing base packages"
    pkg_update
    if [[ "${OS_FAMILY}" == "debian" ]]; then
        # Debian 13/Trixie no longer provides software-properties-common in stable.
        # It is only needed on Ubuntu for add-apt-repository/ondrej/php.
        pkg_install ca-certificates curl wget gnupg lsb-release unzip tar git dnsutils cron openssl python3 sed grep gawk coreutils debconf-utils
        if [[ "${OS_ID}" == "ubuntu" ]]; then
            pkg_install software-properties-common
        fi
    else
        pkg_install ca-certificates curl wget gnupg2 unzip tar git bind-utils cronie openssl python3 sed grep gawk coreutils policycoreutils-python-utils || pkg_install ca-certificates curl wget gnupg2 unzip tar git bind-utils cronie openssl python3 sed grep gawk coreutils
        systemctl enable --now crond >/dev/null 2>&1 || true
    fi
}

fetch_moodle_environment() {
    local tmpxml="/tmp/moodle-environment.xml"
    local outenv="/tmp/moodle-env-requirements.env"

    log "Reading current Moodle requirements from environment.xml"
    curl -fsSL "${ENV_URL}" -o "${tmpxml}" || die "Failed to download ${ENV_URL}"

    python3 - "${tmpxml}" > "${outenv}" <<'PY'
import shlex
import sys
import xml.etree.ElementTree as ET

xml_path = sys.argv[1]
root = ET.parse(xml_path).getroot()
moodles = list(root.iter('MOODLE'))
if not moodles:
    raise SystemExit('Nenhum bloco MOODLE encontrado no environment.xml')

moodle = moodles[-1]

def emit(key, value):
    print(f'{key}={shlex.quote(value or "")}')

php_required = ''
for item in moodle.iter('PHP'):
    if item.attrib.get('level') == 'required':
        php_required = item.attrib.get('version', '').strip()

vendors = {}
for vendor in moodle.iter('VENDOR'):
    name = vendor.attrib.get('name', '').strip().lower()
    if name in {'mariadb', 'mysql'}:
        vendors[name] = vendor.attrib.get('version', '').strip()

emit('MOODLE_VERSION', moodle.attrib.get('version', '').strip())
emit('MOODLE_REQUIRES', moodle.attrib.get('requires', '').strip())
emit('PHP_REQUIRED', php_required)
emit('MARIADB_REQUIRED', vendors.get('mariadb', ''))
emit('MYSQL_REQUIRED', vendors.get('mysql', ''))
PY

    # shellcheck source=/tmp/moodle-env-requirements.env
    source "${outenv}"

    [[ -n "${MOODLE_VERSION}" ]] || die "Could not detect the Moodle version."
    [[ -n "${PHP_REQUIRED}" ]] || die "Could not detect the required PHP version."
    [[ -n "${MARIADB_REQUIRED}" ]] || warn "Could not detect the required MariaDB version in XML."
    [[ -n "${MYSQL_REQUIRED}" ]] || warn "Could not detect the required MySQL version in XML."

    PHP_SERIES="$(printf '%s' "${PHP_REQUIRED}" | awk -F. '{print $1"."$2}')"
    local moodle_major moodle_minor
    moodle_major="${MOODLE_VERSION%%.*}"
    moodle_minor="${MOODLE_VERSION#*.}"
    moodle_minor="${moodle_minor%%.*}"
    if [[ "${moodle_major}" =~ ^[0-9]+$ ]] && [[ "${moodle_minor}" =~ ^[0-9]+$ ]]; then
        MOODLE_STABLE_BRANCH="$(printf 'MOODLE_%d%02d_STABLE' "${moodle_major}" "${moodle_minor}")"
    else
        MOODLE_STABLE_BRANCH="MOODLE_${MOODLE_VERSION/./}_STABLE"
    fi
    log "Moodle ${MOODLE_VERSION} requires PHP >= ${PHP_REQUIRED}, MariaDB >= ${MARIADB_REQUIRED:-?}, MySQL >= ${MYSQL_REQUIRED:-?}"
    save_progress
}

ask_install_options() {
    log "Starting interactive installer questions"

    prompt_database_select
    prompt_domain_box

    local generated_db_root_pass="0"
    if [[ -n "${DB_ROOT_PASS:-}" ]]; then
        log "Using saved/existing database root password."
    else
        DB_ROOT_PASS="$(generate_strong_password)"
        generated_db_root_pass="1"
        save_progress
    fi

    if [[ "${generated_db_root_pass}" == "1" ]]; then
        [[ ${#DB_ROOT_PASS} -ge 16 ]] || die "The generated database root password is unexpectedly short."
        [[ "${DB_ROOT_PASS}" =~ [[:upper:]] ]] || die "The generated database root password must contain at least one uppercase letter."
        [[ "${DB_ROOT_PASS}" =~ [[:lower:]] ]] || die "The generated database root password must contain at least one lowercase letter."
        [[ "${DB_ROOT_PASS}" =~ [[:digit:]] ]] || die "The generated database root password must contain at least one number."
        [[ "${DB_ROOT_PASS}" =~ [^[:alnum:]] ]] || die "The generated database root password must contain at least one special character."
    fi

    save_progress
}

detect_public_ip() {
    PUBLIC_IP="$(curl -fsSL --max-time 8 https://api.ipify.org 2>/dev/null || true)"
    if [[ -z "${PUBLIC_IP}" ]]; then
        PUBLIC_IP="$(curl -fsSL --max-time 8 https://ifconfig.me/ip 2>/dev/null || true)"
    fi
    if [[ -z "${PUBLIC_IP}" ]]; then
        PUBLIC_IP="$(hostname -I | awk '{print $1}')"
    fi
    [[ -n "${PUBLIC_IP}" ]] || die "Could not detect the public IP."
    log "Detected public IP: ${PUBLIC_IP}"
    save_progress
}

check_dns_before_continue() {
    if [[ -z "${PANEL_DOMAIN}" ]]; then
        SERVER_NAME="_"
        BASE_URL="http://${PUBLIC_IP}"
        log "The panel installation will be available by IP: ${BASE_URL}"
        save_progress
        return
    fi

    local resolved=""
    resolved="$(dig +short A "${PANEL_DOMAIN}" | grep -E '^[0-9]+\.[0-9]+\.[0-9]+\.[0-9]+$' | sort -u | tr '\n' ' ' | sed 's/[[:space:]]*$//')"
    if [[ -z "${resolved}" ]]; then
        die "The domain ${PANEL_DOMAIN} does not have a published A record yet. Point DNS to ${PUBLIC_IP} and run this installer again."
    fi

    if ! printf ' %s ' "${resolved}" | grep -q " ${PUBLIC_IP} "; then
        die "DNS mismatch for ${PANEL_DOMAIN}. Resolved: ${resolved}. Expected: ${PUBLIC_IP}. Fix DNS and run this installer again."
    fi

    SERVER_NAME="${PANEL_DOMAIN}"
    BASE_URL="https://${PANEL_DOMAIN}"
    log "DNS verified: ${PANEL_DOMAIN} points to ${PUBLIC_IP}"
    save_progress
}

install_debian_php_default() {
    log "Trying the distribution default PHP first"
    pkg_install php php-cli php-fpm php-mysql php-curl php-xml php-mbstring php-zip php-gd php-intl php-soap php-opcache php-bcmath php-ldap

    PHP_BIN="$(command -v php || true)"
    [[ -n "${PHP_BIN}" ]] || return 1

    local php_current=""
    php_current="$(${PHP_BIN} -r 'echo PHP_VERSION;' 2>/dev/null || true)"
    [[ -n "${php_current}" ]] || return 1

    if ! version_ge "${php_current}" "${PHP_REQUIRED}"; then
        warn "Distribution PHP (${php_current}) is lower than Moodle requires (${PHP_REQUIRED}). A newer PHP repository will be configured."
        return 1
    fi

    PHP_SERIES="$(${PHP_BIN} -r 'echo PHP_MAJOR_VERSION.".".PHP_MINOR_VERSION;' 2>/dev/null)"
    PHP_FPM_SERVICE="php${PHP_SERIES}-fpm"
    PHP_FPM_SOCKET="/run/php/php${PHP_SERIES}-fpm.sock"

    if ! systemctl list-unit-files "${PHP_FPM_SERVICE}.service" >/dev/null 2>&1; then
        warn "Could not find ${PHP_FPM_SERVICE}. The installer will continue and systemctl will report the exact error if PHP-FPM is unavailable."
    fi

    return 0
}

install_debian_php_versioned() {
    if ! apt-cache show "php${PHP_SERIES}-fpm" >/dev/null 2>&1; then
        if [[ "${OS_ID}" == "ubuntu" ]]; then
            log "The default repository did not find php${PHP_SERIES}; adding ondrej/php PPA"
            LC_ALL=C.UTF-8 add-apt-repository -y ppa:ondrej/php
            apt-get update -y
        else
            log "The default repository did not provide a compatible PHP; adding Sury PHP repository for Debian"
            mkdir -p /usr/share/keyrings
            curl -fsSL https://packages.sury.org/php/apt.gpg | gpg --dearmor -o /usr/share/keyrings/deb.sury.org-php.gpg
            chmod 0644 /usr/share/keyrings/deb.sury.org-php.gpg
            local codename=""
            codename="$(lsb_release -sc 2>/dev/null || true)"
            if [[ -z "${codename}" && -r /etc/os-release ]]; then
                # shellcheck source=/dev/null
                source /etc/os-release
                codename="${VERSION_CODENAME:-}"
            fi
            [[ -n "${codename}" ]] || die "Could not detect the Debian codename for the PHP repository."
            cat > /etc/apt/sources.list.d/php-sury.list <<SURYPHP
# Added by Moodle Friendly Installation installer.
