            printf '
Invalid domain: %s.
' "${PANEL_DOMAIN}" >&4
            printf 'Use only a valid domain, without http://, without path and without spaces.
' >&4
            printf 'Example: panel.example.com, or leave blank to use the public IP.
' >&4
            printf 'Press ENTER to type the domain again...' >&4
            if ! IFS= read -r _ <&3; then
                die "Could not read input from the terminal."
            fi
            printf '
' >&4
            PANEL_DOMAIN=""
            PANEL_DOMAIN_SELECTED=0
            save_progress
            prompt_domain_box
            continue
        fi

        local resolved=""
        resolved="$(resolve_domain_a_records "${PANEL_DOMAIN}")"
        if [[ -z "${resolved}" ]]; then
            if [[ "${NONINTERACTIVE}" == "1" ]]; then
                die "The domain ${PANEL_DOMAIN} does not have a published A record yet. Point DNS to ${PUBLIC_IP} and run this installer again."
            fi

            warn "The domain ${PANEL_DOMAIN} does not have a published A record yet."
            printf '
The domain %s does not have a published A record yet.
' "${PANEL_DOMAIN}" >&4
            printf 'Point DNS to %s, or type another domain.
' "${PUBLIC_IP}" >&4
            printf 'Press ENTER to type the domain again...' >&4
            if ! IFS= read -r _ <&3; then
                die "Could not read input from the terminal."
            fi
            printf '
' >&4
            PANEL_DOMAIN=""
            PANEL_DOMAIN_SELECTED=0
            save_progress
            prompt_domain_box
            continue
        fi

        if ! printf ' %s ' "${resolved}" | grep -q " ${PUBLIC_IP} "; then
            if [[ "${NONINTERACTIVE}" == "1" ]]; then
                die "DNS mismatch for ${PANEL_DOMAIN}. Resolved: ${resolved}. Expected: ${PUBLIC_IP}. Fix DNS and run this installer again."
            fi

            warn "DNS mismatch for ${PANEL_DOMAIN}. Resolved: ${resolved}. Expected: ${PUBLIC_IP}."
            printf '
DNS mismatch for %s.
' "${PANEL_DOMAIN}" >&4
            printf 'Resolved: %s
' "${resolved}" >&4
            printf 'Expected: %s
' "${PUBLIC_IP}" >&4
            printf 'Fix DNS, or type another domain.
' >&4
            printf 'Press ENTER to type the domain again...' >&4
            if ! IFS= read -r _ <&3; then
                die "Could not read input from the terminal."
            fi
            printf '
' >&4
            PANEL_DOMAIN=""
            PANEL_DOMAIN_SELECTED=0
            save_progress
            prompt_domain_box
            continue
        fi

        SERVER_NAME="${PANEL_DOMAIN}"
        BASE_URL="https://${PANEL_DOMAIN}"
        log "DNS verified: ${PANEL_DOMAIN} points to ${PUBLIC_IP}"
        save_progress
        return
    done
}

install_debian_php_default() {
    log "Trying the distribution default PHP first"
    pkg_install php php-cli php-fpm php-mysql php-curl php-xml php-mbstring php-zip php-gd php-intl php-soap php-opcache php-bcmath php-ldap

    PHP_BIN="$(command -v php || true)"
