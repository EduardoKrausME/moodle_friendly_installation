        printf 'STEP_PHP=%q\n' "${STEP_PHP:-0}"
        printf 'STEP_DATABASE=%q\n' "${STEP_DATABASE:-0}"
        printf 'STEP_WEB_SERVERS=%q\n' "${STEP_WEB_SERVERS:-0}"
