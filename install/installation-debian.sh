remove_legacy_panel_vhost() {
    local dir="$1"
    local file="$2"
    local target_file="${dir}/admin.moodle.conf"

    [[ -n "${file}" ]] || return 0
    [[ "${file}" != "${target_file}" ]] || return 0
    [[ -f "${file}" ]] || return 0

    if grep -Eq "DocumentRoot[[:space:]]+${INSTALL_DIR}/public|moodle-friendly-installation|proxy_pass[[:space:]]+http://127\\.0\\.0\\.1:8080" "${file}" 2>/dev/null; then
        rm -f "${file}"
    fi
}

cleanup_legacy_panel_vhosts() {
    local dir="$1"
    local current_file="${dir}/admin.moodle.conf"
    local legacy_file

    for legacy_file in "${dir}/${SERVER_NAME}.conf" "${dir}/_.conf"; do
        remove_legacy_panel_vhost "${dir}" "${legacy_file}"
    done

    if [[ -d "${dir}" ]]; then
        while IFS= read -r legacy_file; do
            [[ "${legacy_file}" != "${current_file}" ]] || continue
            remove_legacy_panel_vhost "${dir}" "${legacy_file}"
        done < <(grep -RlE "DocumentRoot[[:space:]]+${INSTALL_DIR}/public|moodle-friendly-installation|proxy_pass[[:space:]]+http://127\\.0\\.0\\.1:8080" "${dir}"/*.conf 2>/dev/null || true)
    fi
}

write_apache_vhost() {
    local file="/etc/apache2/sites-enabled/admin.moodle.conf"
    cleanup_legacy_panel_vhosts "/etc/apache2/sites-enabled"
    local server_for_apache="${PANEL_DOMAIN:-${PUBLIC_IP}}"
    local apache_log_dir="/var/log/apache2"
    cat > "${file}" <<APACHE
<VirtualHost *:8080>
    ServerName ${server_for_apache}
    DocumentRoot ${INSTALL_DIR}/public

    <Directory ${INSTALL_DIR}>
        Require all denied
    </Directory>

    <Directory ${INSTALL_DIR}/public>
        Options FollowSymLinks
        AllowOverride All
        Require all granted
    </Directory>

    <FilesMatch \.php$>
        SetHandler "proxy:unix:${PHP_FPM_SOCKET}|fcgi://localhost/"
    </FilesMatch>

    ErrorLog ${apache_log_dir}/moodle-friendly-installation-error.log
    CustomLog ${apache_log_dir}/moodle-friendly-installation-access.log combined
</VirtualHost>
APACHE
}

write_nginx_vhost() {
    local file="/etc/nginx/sites-enabled/admin.moodle.conf"
    cleanup_legacy_panel_vhosts "/etc/nginx/sites-enabled"
    local listen="listen 80;"
    local server_name="${PANEL_DOMAIN:-_}"
    if [[ -z "${PANEL_DOMAIN}" ]]; then
        listen="listen 80 default_server;"
    fi

    rm -f /etc/nginx/sites-enabled/default /etc/nginx/conf.d/default.conf 2>/dev/null || true

    cat > "${file}" <<NGINX
server {
    ${listen}
    server_name ${server_name};

    client_max_body_size 128m;

    location / {
        proxy_set_header Host \$host;
        proxy_set_header X-Real-IP \$remote_addr;
        proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto \$scheme;
        proxy_set_header X-Forwarded-Host \$host;
        proxy_read_timeout 300;
        proxy_connect_timeout 300;
        proxy_pass http://127.0.0.1:8080;
    }
}
NGINX
}

